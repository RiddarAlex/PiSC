def replaceLabel(input, filename, count):
# Replaces labels Y with labels X instead.
    Ylist = []

    # Put every label into Ylist
    for inner_list in input:
        for element in inner_list:
            Ylist.append(str(element))
        
    # Open and read mesh-file
